    public update_customer() {
        initComponents();
        
        try 
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/banking","root","");
            PreparedStatement pst = con.prepareStatement("select branch_name from branch");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next())
            {
                cb_branch_name.addItem(rs.getString("branch_name"));
            }
            
            pst = con.prepareStatement("select * from customer where customer_id = ?");
            pst.setInt(1, customer_id);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                tf_name.setText(rs.getString("name"));
                tf_address.setText(rs.getString("address"));
                tf_mobile_number.setText(rs.getString("mobile_number"));
            }
            
            pst = con.prepareStatement("select * from account where customer_id = ?");
            pst.setInt(1, customer_id);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                tf_balance.setText(rs.getString("balance"));
                cb_account_type.setSelectedItem(rs.getString("account_type"));
                branch_id = Integer.parseInt(rs.getString("branch_id"));
            }
            
            pst = con.prepareStatement("select branch_name from branch where branch_id = ?");
            pst.setInt(1, branch_id);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                cb_branch_name.setSelectedItem(rs.getString("branch_name"));
            }
        }
        catch(Exception ex)
        {
            System.out.println("Exception : "+ex);
        }
    }

