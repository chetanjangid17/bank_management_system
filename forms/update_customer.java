    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name = tf_name.getText();
        String address = tf_address.getText();
        long mobile_number = Long.parseLong(tf_mobile_number.getText());
        long balance = Long.parseLong(tf_balance.getText());
        String account_type = (String) cb_account_type.getSelectedItem();
        String branch_name = (String) cb_branch_name.getSelectedItem();
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/banking","root","");
            PreparedStatement pst = con.prepareStatement("select branch_id from branch where branch_name = ?");
            pst.setString(1, branch_name);
            ResultSet rs = pst.executeQuery();

            if(rs.next())
            {
                branch_id = Integer.parseInt(rs.getString("branch_id"));
            }
            
            pst = con.prepareStatement("update customer set name = ?, address = ?, mobile_number = ? where customer_id = ?");
            pst.setString(1, name);
            pst.setString(2, address);
            pst.setLong(3, mobile_number);
            pst.setInt(4, customer_id);
            pst.executeUpdate();
            
            pst = con.prepareStatement("update account set balance = ?, account_type = ?, branch_id = ? where customer_id = ?");
            pst.setLong(1, balance);
            pst.setString(2, account_type);
            pst.setInt(3, branch_id);
            pst.setInt(4, customer_id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Customer Record Updated Succesfully");
            this.setVisible(false);
            new admin().setVisible(true);            
        }
        catch(Exception ex)
        {
            System.out.println("Exception : "+ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

